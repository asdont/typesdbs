CREATE TABLE test_types (
    id                      NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    col_varchar2            VARCHAR2(100),
    col_char                CHAR(10),
    col_nvarchar2           NVARCHAR2(100),
    col_number              NUMBER(10, 2),
    col_integer             INTEGER,
    col_float               BINARY_FLOAT,
    col_double              BINARY_DOUBLE,
    col_date                DATE,
    col_timestamp           TIMESTAMP,
    col_timestamp_tz        TIMESTAMP WITH TIME ZONE,
    col_timestamp_ltz       TIMESTAMP WITH LOCAL TIME ZONE,
    col_interval_ym         INTERVAL YEAR TO MONTH,
    col_interval_ds         INTERVAL DAY TO SECOND,
    col_raw                 RAW(16),
    col_blob                BLOB,
    col_clob                CLOB,
    col_nclob               NCLOB,
    col_xml                 XMLTYPE,
    col_boolean             NUMBER(1) CHECK (col_boolean IN (0, 1)),
    col_default_val         VARCHAR2(20) DEFAULT 'DEFAULT',
    col_nullable_by_def     VARCHAR2(50)
) TABLESPACE USERS;

